package com.example.MyBookShopApp.data;

import lombok.Data;

@Data
public class Author {
    private Integer id;
    private String firstName;
    private String lastName;

    @Override
    public String toString() {
        return "Author{" +
                "id=" + id +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                '}';
    }
}
